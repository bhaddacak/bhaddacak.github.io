xrefUtil.getXref = function(snum) {
	let result = [];
	if (snum.startsWith("p"))
		return result;
	for (const list of this.xrefList) {
		if (list.indexOf(snum) > -1)
			result = result.concat(list);
	}
	result = this.unique(result, snum);
	result.sort(this.compareSuttaNumber);
	return result;
};
xrefUtil.unique = function(array, exclude) {
	let result = [];
	for (const item of array) {
		if (item === exclude)
			continue;
		if (result.indexOf(item) === -1)
			result.push(item);
	}
	return result;
};
xrefUtil.breakDecimal = function(numStr) {
	const dotpos = numStr.indexOf(".");
	if (dotpos > -1)
		return [ parseInt(numStr.slice(0, dotpos)), parseInt(numStr.slice(dotpos+1)) ];
	else
		return [ parseInt(numStr), 0 ];

};
xrefUtil.getSuttaNumber = function(head) {
	const book = head.charAt(0).toLowerCase();
	let num = "";
	const firstdot = head.indexOf(".");
	if (book === "m") {
		num = head.slice(5, head.indexOf(".", firstdot+1));
	} else if (book === "p") {
		num = head.slice(6, head.indexOf("]"));
	} else if (book === "k") {
		num = head.slice(5, head.indexOf(","));
	} else {
		num = head.slice(5, firstdot);
	}
	return book + num;
};
xrefUtil.compareSuttaNumber = function(a, b) {
	let result = 0;
	const blist = ["k", "r", "m", "p", "n", "s"];
	const numA = xrefUtil.breakDecimal(a.slice(1));
	const numB = xrefUtil.breakDecimal(b.slice(1));
	const itemA = { book: a.slice(0, 1), int: numA[0], dec: numA[1] };
	const itemB = { book: b.slice(0, 1), int: numB[0], dec: numB[1] };
	const bookDiff = blist.indexOf(itemA.book) - blist.indexOf(itemB.book);
	if (bookDiff === 0) {
		const intDiff = itemA.int - itemB.int;
		if (intDiff === 0)
			result = itemA.dec - itemB.dec;
		else
			result = intDiff;
	} else {
		result = bookDiff;
	}
	return result;
};

